package fundamentos.aulas;

public class A12_OperadoresLogicos {
	public static void main(String[] args) {
	
		System.out.println("Tabelas verdades operadores logicos");
		System.out.println("\nTabela verdade do E (and)");
		System.out.println(true && true);
		System.out.println(true && false);
		System.out.println(false && true);
		System.out.println(false && false);
		System.out.println("\nTabela verdade do ou (Or)");
		System.out.println(true || true);
		System.out.println(true || false);
		System.out.println(false || true);
		System.out.println(false || false);
		System.out.println("\nTabela verdade do ou exclusivo (Xor)");
		System.out.println(true ^ true);
		System.out.println(true ^ false);
		System.out.println(false ^ true);
		System.out.println(false ^ false);
		System.out.println("\nTabela verdade da negação (not)"); //operador unário;
		System.out.println(!true);
		System.out.println(!false);
		System.out.println(!!true);
		System.out.println(!!false);
		
		
		
		
		
		
		
		
		
}
}
